def copier():
    """ Бұл функция берілген файлды оқып, оның `_1` қосылған көшірмесін жасайды. """
    
    # 1️⃣ Пайдаланушыдан файл атауын енгізуді сұраймыз
    string = str(input("Enter the name of file: "))  
    
    # 2️⃣ Файлды ашу және оның мазмұнын оқу
    with open(string) as file:  # Файлды оқу режимінде ашу ('r' - default)
        data = file.read()  # Файлдың барлық мазмұнын `data` айнымалысына сақтау
    
    file.close()  # ❌ Бұл артық код, себебі `with open()` файлды автоматты түрде жабады.
    
    # 3️⃣ Көшірме файл атауын жасау (`_1` қосу)
    copy_path = ""  # Жаңа атауды сақтау үшін бос жол
    for i in range(len(string)):  
        if string[i] == '.':  # Файл атауында '.' кездессе, оның алдына '_1' қосу  
            copy_path += '_1'  
        copy_path += string[i]  # Қалған символдарды өзгеріссіз қосу
    
    # 4️⃣ Көшірме файл жасау және деректерді жазу
    with open(copy_path, "+w") as file_copy:  # Жаңа файлды жазу режимінде ашу
        file_copy.write(data)  # Оқылған деректерді жаңа файлға жазу
    
    file.close()  # ❌ Бұл артық код, себебі `with open()` файлды автоматты түрде жабады.

    return 0  # ✅ Бағдарламаның дұрыс аяқталғанын білдіретін мән (бірақ қажет емес)

# Функцияны орындау
copier()
