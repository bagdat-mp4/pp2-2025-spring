# Тауықтар мен қояндардың бастары мен аяқтарын есептейтін функция
def solve(numheads, numlegs):
    # Тауықтардың санын 0-ден бастап, барлық бастар санына дейін тексеру
    for chickens in range(numheads + 1):
        rabbits = numheads - chickens  # Қояндардың санын есептеу
        # Егер тауықтар мен қояндардың аяқтарының жалпы саны сәйкес келсе
        if 2 * chickens + 4 * rabbits == numlegs:
            return chickens, rabbits  # Тауықтар мен қояндардың санын қайтару
    return None  # Егер шешім табылмаса, None қайтару

# Мысал қолдану:
print(solve(35, 94))  # 35 бас және 94 аяқ үшін тауықтар мен қояндардың санын есептеу