import re  # Регулярлы өрнектер (RegEx) кітапханасын жүктейміз

txt = "abbbbb, arrrrbb"  # Берілген мәтін

# RegEx өрнегі:
# ^      -> Жолдың басын білдіреді (мәтін "a" әрпінен басталуы керек).
# .*     -> Кез келген символдар (нөл немесе одан көп).
# b$     -> "b" әрпімен аяқталуы керек.

x = re.findall(r"^a.*b$", txt)  # RegEx өрнегі арқылы іздеу
print(x)  # Нәтижені шығару
