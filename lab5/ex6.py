import re  # Регулярлы өрнектер (RegEx) кітапханасын жүктейміз

txt = "Bagdat,go to school."  # Берілген мәтін

# RegEx өрнегі:
# [ ,.]  -> Бос орын (" "), үтір (","), немесе нүкте (".") символдарын табады.

pattern = r"[ ,.]"  # Ізделетін символдар тізімі
x = re.sub(pattern, ":", txt)  # Осы символдарды ":" символына ауыстырамыз
print(x)  # Нәтижені шығару
