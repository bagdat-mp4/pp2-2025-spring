def countdown(n):
    """
    Бұл генератор n-ден 0-ге дейінгі барлық сандарды кері ретпен (n, n-1, ..., 0) қайтарады.
    """
    while n >= 0:
        yield n      # Ағымдағы n мәнін қайтарамыз
        n -= 1       # n-ді 1-ге кемітеміз

# Генераторды тестілеу
n = int(input("n: "))  # Пайдаланушыдан сан енгізу сұралады
for num in countdown(n):
    print(num)
