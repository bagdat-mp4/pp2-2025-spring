def d34(n):
    result = []              # Нәтижені сақтау үшін бос тізім жасаймыз.
    for i in range(n):       # 0-ден n-1-ге дейінгі барлық сандарды қарастырамыз.
        # Егер i саны 3-ке және 4-ке бөлінсе (яғни, 12-ге бөлінсе),
        # онда i жұп сан болып табылады.
        if i % 3 == 0 and i % 4 == 0:
            result.append(i)  # Сонда оны нәтижелер тізіміне қосамыз.
    return result            # Цикл аяқталған соң, нәтижелер тізімін қайтарамыз.

# Пайдаланушыдан n санын енгізуді сұраймыз,
# енгізілген мәнді бүтін санға айналдырамыз.
n = int(input("n:"))
# d34 функциясын шақырып, нәтижесін k айнымалысына сақтаймыз.
k = d34(n)
# Алынған нәтижелер тізімін экранға шығарамыз.
print(k)
