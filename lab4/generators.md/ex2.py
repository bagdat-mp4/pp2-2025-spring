def evens(n):
    result = []            # Нәтижені сақтау үшін бос тізім инициализациялаймыз.
    for i in range(n):     # 0-ден n-1-ге дейінгі барлық сандарды аралаймыз.
        if i % 2 == 0:     # Егер i саны жұп болса (2-ге бөлінсе, қалдық 0 болады),
            result.append(i)  # онда оны result тізіміне қосамыз.
    return result          # Цикл аяқталғаннан кейін, result тізімін қайтарамыз.

# Пайдаланушыдан n санын енгізуді сұраймыз, енгізілген мәнді бүтін санға аударамыз.
n = int(input("n:"))
# evens функциясын шақырып, нәтижесін k айнымалысына сақтаймыз.
k = evens(n)
# Алынған жұп сандар тізімін экранға шығарамыз.
print(k)
