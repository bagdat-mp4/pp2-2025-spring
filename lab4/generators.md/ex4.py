def squares(a, b):
    """
    a-дан b-ге дейінгі сандардың квадраттарын генерациялайды.
    """
    for i in range(a, b + 1):  # a-дан b-ге дейін, b де қосылып есепке алынады
        yield i ** 2         # i санының квадратын yield арқылы қайтарамыз

# Генераторды тестілеу:
for square in squares(2, 6):
    print(square)
