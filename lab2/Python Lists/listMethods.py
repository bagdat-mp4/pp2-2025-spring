# 1. append()
# Элементті тізімнің соңына қосады.
my_list = [1, 2, 3]
my_list.append(4)  # Тізім соңына 4 қосылады
print(my_list)  # Нәтиже: [1, 2, 3, 4]

# 2. clear()
# Тізімнің барлық элементтерін өшіреді.
my_list = [1, 2, 3]
my_list.clear()  # Тізім бос болады
print(my_list)  # Нәтиже: []

# 3. copy()
# Тізімнің көшірмесін қайтарады.
my_list = [1, 2, 3]
new_list = my_list.copy()  # Тізімнің жаңа көшірмесін жасайды
print(new_list)  # Нәтиже: [1, 2, 3]

# 4. count()
# Белгілі бір мәннің тізімде неше рет кездесетінін санайды.
my_list = [1, 2, 2, 3]
print(my_list.count(2))  # 2 санының кездесетінін санайды, Нәтиже: 2

# 5. extend()
# Басқа тізімнің элементтерін ағымдағы тізімнің соңына қосады.
my_list = [1, 2]
my_list.extend([3, 4])  # Тізімге [3, 4] қосылады
print(my_list)  # Нәтиже: [1, 2, 3, 4]

# 6. index()
# Белгілі бір мәннің тізімдегі алғашқы индексін қайтарады.
my_list = [1, 2, 3]
print(my_list.index(2))  # 2 мәнінің индексін қайтарады, Нәтиже: 1

# 7. insert()
# Тізімнің белгілі бір орнына элемент қосады.
my_list = [1, 2, 4]
my_list.insert(2, 3)  # 2-индекске 3 қосылады
print(my_list)  # Нәтиже: [1, 2, 3, 4]

# 8. pop()
# Белгілі бір индекстегі элементті өшіреді (немесе соңғы элементті).
my_list = [1, 2, 3]
my_list.pop()  # Соңғы элементті өшіреді
print(my_list)  # Нәтиже: [1, 2]

# 9. remove()
# Белгілі бір мәнді тізімнен өшіреді.
my_list = [1, 2, 3]
my_list.remove(2)  # 2 мәнін өшіреді
print(my_list)  # Нәтиже: [1, 3]

# 10. reverse()
# Тізімдегі элементтердің ретін кері айналдырады.
my_list = [1, 2, 3]
my_list.reverse()  # Тізімді кері айналдырады
print(my_list)  # Нәтиже: [3, 2, 1]

# 11. sort()
# Тізімді өсу ретімен сұрыптайды.
my_list = [3, 1, 2]
my_list.sort()  # Тізімді сұрыптайды
print(my_list)  # Нәтиже: [1, 2, 3]
